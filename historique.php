                            <div class="about-text">
                                <p>
                                Le Bureau d’Etudes, de Recherche et d’Engineering Général (B.E.R.E.G) est une entreprise publique économique, société par actions au capital de 200.000.000 DA dont le siège est situé à BAB EZZOUAR ALGER, inscrit au registre de commerce sous le numéro RC 0014450 B00.
                                </p>
                                <br/>
                                <p>
                                Le B.E.R.E.G a été créé en 1968 pour servir d’outil d’étude et de suivi de la réalisation des programmes de construction lancés par l’état Algérien et inscrits dans ses différents plans économiques. Il est issu de la prestigieuse Direction Nationale de Construction (DNC).
                                Il a ensuite été restructuré et agrée par le Décret Présidentiel N° 82-73 du 20/02/82. <br>
                                Après avoir fait partie du portefeuille du Groupe INJACO, relevant de la SGP/INDJAB, il a été directement rattaché à la SGP INJAB (Société de Gestion des Participations).
                                
                                </p>
                                <br/>
                                <p>
                                Suite à la restructuration des SGP, le B.E.R.E.G a été transféré à la SGP GENEST avant la transformation de cette dernière en « Groupe Etudes et Engineering » le 15 Février 2018. 
                                Le B.E.R.E.G a été associé, seul ou dans le cadre de partenariat, avec des groupes de différentes nationalités à la conception, au contrôle et au suivi de la réalisation dans la plupart des grandes infrastructures réalisées dans le cadre du programme de développement national. Il a été l’initiateur dans l’introduction de nouvelles techniques de construction en Algérie en collaboration avec des partenaires étrangers dans les domaines suivants :    
                                <br/> &nbsp •   Précontraint, Traditionnel, Tridimensionnel, Coffrage tunnel Préfabrication lourde et légère, Lamellé collé
                                <br/> &nbsp •	Utilisation de fondations spéciales (Pieux Etc.)
                                </p>
                                <p> 
                                Le B.E.R.E.G a été et demeure encore une « institution » d’encadrement de stages pratiques et d’appui à la formation dispensée par les universités et les grandes écoles spécialisées, tels que :
                                </p>
                                <p> 
                                <br/> &nbsp •	L’Ecole Nationale Polytechnique (E.N.P.A) ;
                                <br/> &nbsp •	L’Ecole Polytechnique d'Architecture et d'Urbanisme (E.P.A.U) ;
                                <br/> &nbsp •	L’Ecole Nationale des Travaux Publics (E.N.T.P) ;
                                <br/> &nbsp •	L’Université des Sciences et Technologies Houari-Boumédiène (USTHB) ;
                                <br/> &nbsp •	L’Institut National de Formation en Bâtiment (INFORBA) ;
                                <br/> &nbsp •	L’Institut National de l’Hydraulique (I.N.H) ;
                                <br/> &nbsp •	L’Institut National d’Informatique (I.N.I)
                                </p>
                                <br/>
